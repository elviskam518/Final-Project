from __future__ import annotations

from pathlib import Path
from typing import Any

import pandas as pd


ROOT = Path(__file__).resolve().parents[2]
LATENT_DIR = ROOT / "latent_vis"


def _read_csv_if_exists(path: Path) -> pd.DataFrame | None:
    if path.exists():
        return pd.read_csv(path)
    return None


def load_method_comparison() -> list[dict[str, Any]]:
    rows: list[dict[str, Any]] = []

    summary_df = _read_csv_if_exists(ROOT / "tech_adversarial_grl_summary.csv")
    if summary_df is not None:
        for _, row in summary_df.iterrows():
            method = str(row["Method"])
            label_map = {
                "Baseline": "Baseline MLP (c.py)",
                "Gender_Adversarial": "Standalone adversarial baseline (c.py, gender)",
                "Intersectional_Adversarial": "Standalone adversarial baseline (c.py, intersectional)",
            }
            rows.append(
                {
                    "method": label_map.get(method, method),
                    "accuracy": float(row.get("Accuracy", float("nan"))),
                    "f1": float(row.get("F1_Score", float("nan"))),
                    "min_di": float(row.get("Min_DI", float("nan"))),
                    "source": "Generated by c.py",
                }
            )

    # Fair CVAE rows are intentionally marked as live-job only in this page.
    rows.extend(
        [
            {
                "method": "Fair CVAE adv_only (more strict .py)",
                "accuracy": None,
                "f1": None,
                "min_di": None,
                "source": "Run from Interactive Demo as background job",
            },
            {
                "method": "Fair CVAE no_adv (more strict .py)",
                "accuracy": None,
                "f1": None,
                "min_di": None,
                "source": "Run from Interactive Demo as background job",
            },
            {
                "method": "Fair CVAE full (more strict .py)",
                "accuracy": None,
                "f1": None,
                "min_di": None,
                "source": "Run from Interactive Demo as background job",
            },
        ]
    )

    return rows


def load_per_group_di_comparison() -> list[dict[str, Any]]:
    di_df = _read_csv_if_exists(ROOT / "tech_adversarial_grl_di_comparison.csv")
    if di_df is None:
        return []
    return di_df.to_dict(orient="records")


def load_latent_summary() -> dict[str, Any]:
    image_paths: list[str] = []
    if LATENT_DIR.exists():
        image_paths = [f"/latent_vis/{p.name}" for p in sorted(LATENT_DIR.glob("*.png"))]

    note = (
        "Latent visual outputs are listed when generated by real runs that invoke latent_vis.py "
        "(e.g., Fair CVAE jobs)."
    )
    return {"rows": [], "note": note, "images": image_paths}
